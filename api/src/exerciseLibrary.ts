// ----------------------------------------------------------------------------
// Filtering & scoring selection logic
// ----------------------------------------------------------------------------

export function filterExercisesForSlot(args: {
  pattern: Pattern;
  constraints: UserConstraints;
}): Exercise[] {
  const { pattern, constraints } = args;

  return EXERCISE_LIBRARY.filter(ex => {
    if (!ex.patterns.includes(pattern)) return false;

    // Experience
    if (lvRank(ex.minLevel) > lvRank(constraints.experience)) return false;

    // Equipment (optional)
    if (constraints.equipmentAvailable?.length) {
      const ok = ex.equipment.some(eq => constraints.equipmentAvailable!.includes(eq));
      if (!ok) return false;
    }

    // Avoid joint flags (optional)
    if (constraints.avoid?.length && ex.jointFlags?.length) {
      if (ex.jointFlags.some(f => constraints.avoid!.includes(f))) return false;
    }

    return true;
  });
}

function clamp(n: number, min: number, max: number) {
  return Math.max(min, Math.min(max, n));
}

// Higher score = better candidate.
export function scoreExercise(args: {
  ex: Exercise;
  ctx: CheckinContext;
  constraints: UserConstraints;
  usedIds?: Set<string>;
}): number {
  const { ex, ctx, usedIds } = args;

  let s = 0;

  // 1) Avoid repeats inside same workout
  if (usedIds?.has(ex.id)) s -= 80;

  // 2) History avoidance across sessions
  const hist = ctx.historyAvoidance;
  if (hist?.recentExerciseIds?.length) {
    const recent = new Set(hist.recentExerciseIds);
    const isRecent = recent.has(ex.id);

    if (isRecent && hist.mode === "hard") return -1e9; // exclude
    if (isRecent && hist.mode === "soft") s -= 120; // strong penalty
  }

  // 3) Intent
  if (ctx.intent === "light") s += (6 - ex.difficulty) * 2;
  if (ctx.intent === "normal") s += ex.difficulty === 3 ? 3 : ex.difficulty === 2 ? 2 : ex.difficulty === 4 ? 1 : 0;
  if (ctx.intent === "hard") s += ex.difficulty * 2;

  // 4) Setup & stability
  const avoidSetup = ctx.avoidHighSetupWhenTired ?? (ctx.intent === "light");
  const setupPenalty = ctx.intent === "hard" ? 1 : ctx.intent === "normal" ? 2 : 3;
  s -= (ex.setupCost - 1) * (avoidSetup ? setupPenalty : Math.max(1, setupPenalty - 1));

  const stabilityPenalty = ctx.intent === "hard" ? 1 : ctx.intent === "normal" ? 2 : 3;
  s -= (ex.stabilityDemand - 1) * stabilityPenalty;

  // 45 min: prefer faster transitions
  if (ctx.timeBucket === 45) {
    s -= (ex.setupCost - 1) * 2;
    if (ex.tags?.includes("not_for_circuit")) s -= 4;
  }

  // 5) Goal biases
  if (ctx.goal === "strength") {
    if (ex.tags?.includes("strength_bias")) s += 7;
    if (ex.kind === "isolation") s -= 3;
    if (ex.repRangeDefault.min >= 10) s -= 2;
  }
  if (ctx.goal === "hypertrophy") {
    if (ex.tags?.includes("hypertrophy_bias")) s += 5;
    if (ex.kind === "isolation") s += 2;
  }
  if (ctx.goal === "fat_loss") {
    if (ex.tags?.includes("good_for_circuit")) s += 6;
    if (ex.tags?.includes("fat_loss_friendly")) s += 3;
    if (ex.tags?.includes("not_for_circuit")) s -= 6;
  }
  if (ctx.preferCircuits) {
    if (ex.tags?.includes("good_for_circuit")) s += 4;
    if (ex.setupCost >= 4) s -= 4;
  }

  // 6) Muscle bias (weak points / priorities)
  if (ctx.muscleBias) {
    for (const m of ex.primaryMuscles) {
      const w = ctx.muscleBias[m];
      if (typeof w === "number") s += w * 6;
    }
    if (ex.secondaryMuscles) {
      for (const m of ex.secondaryMuscles) {
        const w = ctx.muscleBias[m];
        if (typeof w === "number") s += w * 2;
      }
    }
  }

  // 7) Small “novelty” push
  if (ctx.intent === "light" && ex.tags?.includes("stable_choice")) s += 2;
  if (ctx.intent === "hard" && ex.tags?.includes("barbell_skill")) s += 2;

  return s;
}

// Pick N distinct exercises for a pattern using scoring + controlled randomness.
export function pickExercises(args: {
  pattern: Pattern;
  n: number;
  constraints: UserConstraints;
  ctx: CheckinContext;
  excludeIds?: string[];
}): Exercise[] {
  const pool = filterExercisesForSlot({ pattern: args.pattern, constraints: args.constraints }).filter(
    ex => !args.excludeIds?.includes(ex.id)
  );

  const used = new Set<string>(args.excludeIds ?? []);

  let ranked = pool
    .map(ex => ({
      ex,
      score: scoreExercise({ ex, ctx: args.ctx, constraints: args.constraints, usedIds: used }),
    }))
    .filter(x => x.score > -1e8)
    .sort((a, b) => b.score - a.score);

  // If pool too small due to hard history avoidance, relax to soft.
  if (ranked.length < args.n && args.ctx.historyAvoidance?.mode === "hard") {
    const relaxed: CheckinContext = {
      ...args.ctx,
      historyAvoidance: { ...args.ctx.historyAvoidance, mode: "soft" },
    };
    ranked = pool
      .map(ex => ({ ex, score: scoreExercise({ ex, ctx: relaxed, constraints: args.constraints, usedIds: used }) }))
      .sort((a, b) => b.score - a.score);
  }

  // Take top K then diversify with light randomness
  const K = Math.min(ranked.length, Math.max(14, args.n * 6));
  const top = ranked.slice(0, K);

  const out: Exercise[] = [];
  while (out.length < args.n && top.length > 0) {
    const window = top.slice(0, Math.min(8, top.length));
    const weights = window.map(w => clamp(Math.round(w.score + 20), 1, 200));
    const sum = weights.reduce((a, b) => a + b, 0);
    let r = Math.floor(Math.random() * sum);

    let idx = 0;
    for (let i = 0; i < window.length; i++) {
      r -= weights[i];
      if (r < 0) { idx = i; break; }
    }

    const chosen = window[idx].ex;
    if (!used.has(chosen.id)) {
      out.push(chosen);
      used.add(chosen.id);
    }

    const pos = top.findIndex(x => x.ex.id === chosen.id);
    if (pos >= 0) top.splice(pos, 1);
    else top.shift();
  }

  return out;
}
